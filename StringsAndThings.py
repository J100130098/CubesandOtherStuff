# Repetition operator is the multiplier: *
print("Hip "*2 + "Hooray")
def rowYourBoat():
    print("Row "*3 + "your boat")
    print("gently down the stream")
    print("merrily, "*4)
    print("life is but a dream")

print(rowYourBoat())